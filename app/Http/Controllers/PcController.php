     */
    public function index()
    {
        $itemproduk = Product::orderBy('created_at', 'desc')->paginate(20);
        $data = array('title' => 'Produk',
                    'itemproduk' => $itemproduk);
        return view('pcmodel.pcmodel', $data);
    }

    public function produkdetail($id) {
        $itemproduk = Product::where('id', $id)                            
                            ->first();
        if ($itemproduk) {
            $data = array('title' => $itemproduk->name,
                        'itemproduk' => $itemproduk);
            return view('pcmodel.produkdetail', $data);            
        } else {
            // kalo produk ga ada, jadinya tampil halaman tidak ditemukan (error 404)
            return abort('404');
        }
    }

